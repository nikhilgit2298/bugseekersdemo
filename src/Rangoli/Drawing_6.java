package Rangoli;

public class Drawing_6 {

	public static void main(String[] args) {
		for(int i=1;i<=7;i++) {
			for(int b=1;b<=i;b++) {
				System.out.print(" ");       //space
			}
			for(int c=1;c<=1;c++) {
				System.out.print(" *");                   //star
			}
			System.out.println("");
		}			


	}

}
