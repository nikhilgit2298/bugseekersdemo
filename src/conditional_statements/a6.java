package conditional_statements;

public class a6 {     //switch statements

	public static void main(String[] args) {
		int a=1000;
		
		switch(a) {
		case 100 : System.out.println("case 100");
		break;
		case 200 : System.out.println("case 200");
        break;
		case 300 : System.out.println("case 300");
		break;
		case 400 : System.out.println("case 400");
		break;
		
		default :  System.out.println("case default");
		
		
		}
		

	}

}
