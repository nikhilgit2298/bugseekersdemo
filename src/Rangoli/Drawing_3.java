package Rangoli;

public class Drawing_3 {

	
		public static void main(String[] args) {
			for(int i=1;i<=7;i++) {
				for(int b=1;b<=i;b++) {
					System.out.print(" ");   //space condition
				}
				for(int c=7;c>=i;c--) {
					System.out.print(" *");
				}
			System.out.println("");
			}			
			
		}
	}

	


